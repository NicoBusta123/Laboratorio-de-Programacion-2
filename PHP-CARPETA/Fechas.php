/*
========================================================
            FORMATOS MÁS USADOS
========================================================
*/

/*
d  -> día
m  -> mes
Y  -> año completo
y  -> año corto
H  -> hora 24hs
h  -> hora 12hs
i  -> minutos
s  -> segundos
*/

/*
--------------------------------------------------------
Ejemplos
--------------------------------------------------------
*/

