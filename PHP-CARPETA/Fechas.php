/*
========================================================
            OBJETO DateTime
========================================================
*/

/*
Más moderno y recomendado.
*/

$fecha = new DateTime();

echo $fecha->format("d/m/Y");

/*
========================================================
            SUMAR DÍAS CON DateTime
========================================================
*/

$fecha = new DateTime();

$fecha->modify("+5 days");

echo $fecha->format("d/m/Y");

/*
========================================================
            RESTAR DÍAS
========================================================
*/

