<?php

/*
========================================================
            CHEAT SHEET - CADENAS EN PHP
========================================================

String = cadena de texto

========================================================
                CREAR CADENAS
========================================================
*/

$nombre = "Nicolas";

echo $nombre;

/*
--------------------------------------------------------
Comillas dobles
--------------------------------------------------------

Interpretan variables
*/

$edad = 20;

echo "Tengo $edad años";

/*
--------------------------------------------------------
Comillas simples
--------------------------------------------------------

NO interpretan variables
*/

echo 'Tengo $edad años';

/*
========================================================
            CONCATENACIÓN
========================================================
*/

$nombre = "Nico";
$apellido = "Bustamante";

$completo = $nombre . " " . $apellido;

echo $completo;

/*
========================================================
            LONGITUD DE CADENA
========================================================
*/

$texto = "Hola";

echo strlen($texto);

/*
Resultado:
4
*/

/*
========================================================
            MAYÚSCULAS Y MINÚSCULAS
========================================================
*/

/*
--------------------------------------------------------
strtoupper()
Mayúsculas
--------------------------------------------------------
*/

echo strtoupper("hola");

/*
--------------------------------------------------------
strtolower()
Minúsculas
--------------------------------------------------------
*/

echo strtolower("HOLA");

/*
--------------------------------------------------------
ucfirst()
Primera letra mayúscula
--------------------------------------------------------
*/

echo ucfirst("nicolas");

/*
--------------------------------------------------------
ucwords()
Cada palabra con mayúscula
--------------------------------------------------------
*/

echo ucwords("hola mundo");

/*
========================================================
            ELIMINAR ESPACIOS
========================================================
*/

/*
--------------------------------------------------------
trim()
Elimina espacios al inicio y final
--------------------------------------------------------
*/

$texto = "   hola   ";

echo trim($texto);

/*
========================================================
            BUSCAR TEXTO
========================================================
*/

/*
--------------------------------------------------------
strpos()
Busca posición de texto
--------------------------------------------------------
*/

$texto = "Hola Mundo";

echo strpos($texto, "Mundo");

/*
Resultado:
5
*/

/*
--------------------------------------------------------
str_contains()
Verifica si contiene texto
(PHP 8+)
--------------------------------------------------------
*/

if(str_contains($texto, "Hola"))
{
    echo "Existe";
}

/*
========================================================
            REEMPLAZAR TEXTO
========================================================
*/

/*
--------------------------------------------------------
str_replace()
--------------------------------------------------------
*/

$texto = "Hola Mundo";

$nuevo = str_replace("Mundo", "PHP", $texto);

echo $nuevo;

/*
Resultado:
Hola PHP
*/

/*
========================================================
            SUBCADENAS
========================================================
*/

/*
--------------------------------------------------------
substr()
--------------------------------------------------------
*/

$texto = "Programacion";

echo substr($texto, 0, 4);

/*
Resultado:
Prog
*/

/*
========================================================
            DIVIDIR CADENA
========================================================
*/

/*
--------------------------------------------------------
explode()
Convierte string -> array
--------------------------------------------------------
*/

$texto = "rojo,verde,azul";

$colores = explode(",", $texto);

print_r($colores);

/*
========================================================
            UNIR CADENAS
========================================================
*/

/*
--------------------------------------------------------
implode()
Convierte array -> string
--------------------------------------------------------
*/

$frutas = ["manzana", "banana", "pera"];

$texto = implode("-", $frutas);

echo $texto;

/*
Resultado:
manzana-banana-pera
*/

/*
========================================================
            COMPARAR CADENAS
========================================================
*/

/*
--------------------------------------------------------
==
--------------------------------------------------------
*/

$texto1 = "hola";
$texto2 = "hola";

if($texto1 == $texto2)
{
    echo "Iguales";
}

/*
--------------------------------------------------------
strcmp()
--------------------------------------------------------
*/

echo strcmp("hola", "hola");

/*
Resultado:
0 -> iguales
*/

/*
========================================================
            ESCAPAR HTML
========================================================
*/

/*
--------------------------------------------------------
htmlspecialchars()
--------------------------------------------------------

Evita interpretación HTML.
Muy importante en formularios.
*/

$texto = "<h1>Hola</h1>";

echo htmlspecialchars($texto);

/*
Resultado:
&lt;h1&gt;Hola&lt;/h1&gt;
*/

/*
========================================================
            CONVERTIR TIPOS
========================================================
*/

/*
--------------------------------------------------------
String -> número
--------------------------------------------------------
*/

$numero = "20";

echo (int)$numero + 5;

/*
--------------------------------------------------------
Número -> string
--------------------------------------------------------
*/

$edad = 20;

$texto = (string)$edad;

/*
========================================================
            INTERPOLACIÓN
========================================================
*/

/*
Variables dentro de strings
*/

$nombre = "Nico";

echo "Hola $nombre";

/*
También:
*/

echo "Hola {$nombre}";

/*
========================================================
            SALTOS DE LÍNEA
========================================================
*/

/*
En HTML:
*/

echo "Hola<br>Mundo";

/*
En consola:
*/

echo "Hola\nMundo";

/*
========================================================
            CARACTERES ESPECIALES
========================================================
*/

echo "\"Hola\"";

echo "\n";

echo "\t";

/*
========================================================
            STRING A ARRAY
========================================================
*/

$texto = "Hola";

$array = str_split($texto);

print_r($array);

/*
Resultado:
[H, o, l, a]
*/

/*
========================================================
            REPETIR CADENA
========================================================
*/

/*
--------------------------------------------------------
str_repeat()
--------------------------------------------------------
*/

echo str_repeat("PHP ", 3);

/*
Resultado:
PHP PHP PHP
*/

/*
========================================================
            INVERTIR CADENA
========================================================
*/

/*
--------------------------------------------------------
strrev()
--------------------------------------------------------
*/

echo strrev("Hola");

/*
Resultado:
aloH
*/

/*
========================================================
            FORMATEAR TEXTO
========================================================
*/

/*
--------------------------------------------------------
sprintf()
--------------------------------------------------------
*/

$nombre = "Nico";
$edad = 20;

$texto = sprintf(
    "Me llamo %s y tengo %d años",
    $nombre,
    $edad
);

echo $texto;

/*
========================================================
            RESUMEN RÁPIDO
========================================================

strlen()          -> longitud
strtoupper()      -> mayúsculas
strtolower()      -> minúsculas
trim()            -> eliminar espacios
strpos()          -> buscar texto
str_replace()     -> reemplazar
substr()          -> subcadena
explode()         -> string a array
implode()         -> array a string
htmlspecialchars()-> escapar HTML
strrev()          -> invertir texto
str_repeat()      -> repetir texto

========================================================
*/

?>